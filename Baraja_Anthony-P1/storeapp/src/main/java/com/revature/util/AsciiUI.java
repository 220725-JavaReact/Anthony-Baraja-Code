	public static String printProductType(String prodType) {
		if(prodType.equals("CPU")) {
			return printCPUArt();
		} else if (prodType.equals("Motherboard")) {
			return printMotherboardArt();
		} else if (prodType.equals("RAM")) {
			return printRAMArt();
		} else if (prodType.equals("Graphics Card")) {
			return printGPUArt();
		} else if (prodType.equals("Computer Case")) {
			return printCaseArt();
		} else if (prodType.equals("Power Supply")) {
			return printPSUArt();
		} else if (prodType.equals("Internal Drive")) {
			return printDataStorageArt();
		} else if (prodType.equals("Cooling Unit")) {
			return printCoolingUnitArt();
		} else if (prodType.equals("Sound Card")) {
			return printSoundCardArt();
		}

		return "";
	}

